class Relation{
    public static void main(String args[]){
        int a=10;
        int b=5;
        // to check a=b it returns true else it return false 
        System.out.println("\n Equal to operator: " +(a==b));

        // checking is value is not equal then it returns true else false 
        System.out.println("\n Not equal to operator: "+(a!=b));

        // greater than operator 
        System.out.println("\n greater than operator a>b: "+(a>b));

        // less than operator
        System.out.println("\n less than operator a<b: "+(a<b));

        int c=5;
        int d=5;
        System.out.println("\n Equal to operator : "+(c==d));
        System.out.println("\n Not equal to operator: "+(c!=d));
        System.out.println("\n greater than operator c>d: "+(c>d));
        System.out.println("\n less than operator c<d: "+(c<d));


    }
}